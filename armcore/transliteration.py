"""
Транслитерация латиница -> кириллица.

ТЗ, п.3.1: «Данные автоматически переводятся с латиницы на кириллицу
(транслитерация по ГОСТ 7.79-2000, система Б, с возможностью ручной правки)».

ГОСТ 7.79-2000 (система Б) описывает прямое преобразование кириллица -> латиница
через диграфы без диакритики (ж=zh, ч=ch, ш=sh, щ=shh, ц=cz/c, х=x, ё=yo, ю=yu,
я=ya, ы=y`, э=e`). Здесь реализовано ОБРАТНОЕ преобразование (lat -> cyr) методом
«жадного» сопоставления самых длинных сочетаний.

На практике машиночитаемая зона паспорта (ICAO Doc 9303) часто использует не
ГОСТ-Б, а национальную/икаовскую романизацию (kh, ts, shch, kh, yo...). Поэтому,
помимо канонических форм ГОСТ-Б, таблица принимает и распространённые паспортные
диграфы. Любой результат можно поправить вручную в интерфейсе.
"""
from __future__ import annotations

from typing import List, Tuple


# (латиница, кириллица). Порядок не важен — ниже сортируем по длине ключа.
# Сначала канонический ГОСТ 7.79-2000 система Б, затем частые паспортные варианты.
_PAIRS: List[Tuple[str, str]] = [
    # --- многобуквенные сочетания (важен длинный матч в первую очередь) ---
    ("shch", "щ"),   # частая паспортная форма щ
    ("shh", "щ"),    # ГОСТ-Б: щ
    ("sch", "щ"),    # встречается в загранпаспортах
    ("zh", "ж"),
    ("kh", "х"),     # паспортная форма х
    ("ts", "ц"),     # паспортная форма ц
    ("cz", "ц"),     # ГОСТ-Б: ц
    ("ch", "ч"),
    ("sh", "ш"),
    ("yo", "ё"),
    ("jo", "ё"),
    ("yu", "ю"),
    ("ju", "ю"),
    ("ya", "я"),
    ("ja", "я"),
    ("ye", "е"),     # начальное «е» в паспортах
    ("je", "е"),
    ("yi", "и"),
    ("iy", "ий"),    # окончание фамилий -ий
    ("ij", "ий"),
    ("y'", "ы"),
    ("y`", "ы"),     # ГОСТ-Б: ы
    ("e'", "э"),
    ("e`", "э"),     # ГОСТ-Б: э
    ("''", "ъ"),
    # --- одиночные буквы ---
    ("a", "а"),
    ("b", "б"),
    ("v", "в"),
    ("w", "в"),
    ("g", "г"),
    ("d", "д"),
    ("e", "е"),
    ("z", "з"),
    ("i", "и"),
    ("j", "й"),      # ГОСТ-Б: й
    ("k", "к"),
    ("l", "л"),
    ("m", "м"),
    ("n", "н"),
    ("o", "о"),
    ("p", "п"),
    ("q", "к"),
    ("r", "р"),
    ("s", "с"),
    ("t", "т"),
    ("u", "у"),
    ("f", "ф"),
    ("x", "х"),      # ГОСТ-Б: х
    ("c", "ц"),      # ГОСТ-Б: ц (одиночная c)
    ("h", "х"),
    ("y", "ы"),      # неоднозначно (й/ы) — правится вручную
    ("'", "ь"),
    ("`", "ь"),
]

# Сортируем по убыванию длины латинского ключа — для жадного longest-match.
_SORTED_PAIRS = sorted(_PAIRS, key=lambda p: len(p[0]), reverse=True)
_MAX_KEY = max(len(p[0]) for p in _SORTED_PAIRS)

# Быстрый индекс: длина -> {латиница: кириллица}
_BY_LEN = {}
for _lat, _cyr in _SORTED_PAIRS:
    _BY_LEN.setdefault(len(_lat), {})[_lat] = _cyr


def _match_case(src: str, dst: str) -> str:
    """Перенести регистр исходного латинского куска на кириллический результат."""
    if src.isupper() and len(src) > 1:
        return dst.upper()
    if src[:1].isupper():
        return dst[:1].upper() + dst[1:]
    return dst


def translit_lat_to_cyr(text: str) -> str:
    """Транслитерировать строку с латиницы на кириллицу (ГОСТ 7.79-2000, сист. Б).

    Символы, не входящие в таблицу (пробелы, дефисы, цифры), переносятся как есть.
    """
    if not text:
        return ""
    result: List[str] = []
    i = 0
    n = len(text)
    while i < n:
        matched = False
        # Пробуем самые длинные сочетания первыми.
        for length in range(min(_MAX_KEY, n - i), 0, -1):
            chunk = text[i:i + length]
            table = _BY_LEN.get(length)
            if not table:
                continue
            cyr = table.get(chunk.lower())
            if cyr is not None:
                result.append(_match_case(chunk, cyr))
                i += length
                matched = True
                break
        if not matched:
            result.append(text[i])  # неизвестный символ — без изменений
            i += 1
    return "".join(result)


def to_title_name(text: str) -> str:
    """Привести имя из MRZ (ВСЁ КАПСОМ) к виду «Иванов-Петров Анна».

    Учитывает дефисы и пробелы как разделители частей имени.
    """
    if not text:
        return ""

    def cap_word(w: str) -> str:
        return w[:1].upper() + w[1:].lower() if w else w

    out = []
    word = ""
    for ch in text:
        if ch in (" ", "-"):
            out.append(cap_word(word))
            out.append(ch)
            word = ""
        else:
            word += ch
    out.append(cap_word(word))
    return "".join(out)


def transliterate_name(latin_name: str) -> str:
    """Удобная обёртка: транслитерировать ФИО из MRZ и привести к Title Case."""
    return to_title_name(translit_lat_to_cyr(latin_name))


# --- МЧЗ внутреннего паспорта РФ: «модернизированный клер» ---
# Приказ МВД России N 773, прил. 24 (ранее N 851/ФМС N 279): русские буквы
# кодируются латиницей и ЦИФРАМИ. Обратная таблица символ -> кириллица.
_KLER = {
    "A": "А", "B": "Б", "V": "В", "G": "Г", "D": "Д", "E": "Е", "2": "Ё",
    "J": "Ж", "Z": "З", "I": "И", "Q": "Й", "K": "К", "L": "Л", "M": "М",
    "N": "Н", "O": "О", "P": "П", "R": "Р", "S": "С", "T": "Т", "U": "У",
    "F": "Ф", "H": "Х", "C": "Ц", "3": "Ч", "4": "Ш", "W": "Щ", "X": "Ъ",
    "Y": "Ы", "9": "Ь", "6": "Э", "7": "Ю", "8": "Я",
}


def translit_mrz_ru_internal(text: str) -> str:
    """МЧЗ внутреннего паспорта РФ -> кириллица (посимвольно, «клер»)."""
    return "".join(_KLER.get(ch.upper(), ch) for ch in (text or ""))


def transliterate_name_ru_internal(mrz_name: str) -> str:
    """ФИО из МЧЗ внутреннего паспорта РФ -> «Иванов» (Title Case)."""
    return to_title_name(translit_mrz_ru_internal(mrz_name))
